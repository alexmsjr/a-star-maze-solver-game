import numpy as np
import random as rd


### RANDOM MAZE GENERATOR
def gen_randon_maze(nx, ny, maze_in, maze_out, walls):
    maze = np.zeros((nx, ny), int)

    maze[maze_in[0]][maze_in[1]] = 2  # defining maze entrance
    maze[maze_out[0]][maze_out[1]] = 3  # defining maze exit

    counter = 0
    while counter < walls:
        x = rd.randrange(0, nx - 1)
        y = rd.randrange(0, ny - 1)

        if maze[x][y] == 0:
            maze[x][y] = 1
            counter += 1
    return maze


